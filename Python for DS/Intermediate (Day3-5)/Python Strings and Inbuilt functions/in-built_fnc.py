#Python Strings and its inbuilt functions:
#capitalize() function
name = 'nithin'
print(name.capitalize())
#upper() function
print(name.upper())
#lower() function
print(name.lower())
#title() function
print(name.title())
#swapcase() function
print(name.swapcase())
#rpartition() function
print(name.rpartition('i'))

